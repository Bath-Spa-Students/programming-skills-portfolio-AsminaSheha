#Write a Python program to iterate through the keys of a dictionary and print them.

# Sample dictionary
my_dictionary = {
    "name": "Asmina Shehzadha",
    "age": 19,
    "city": "Dubai",
    "occupation": "Student"
}

# Iterate through the keys and print them
print("Keys in the dictionary:")
for key in my_dictionary.keys():
    print(key)
